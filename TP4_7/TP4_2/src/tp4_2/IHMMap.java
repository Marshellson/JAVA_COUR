    private void traitementBornes(){
        // Largueur de la map : 1800
        // Hauteur de la map : 1038
        // Largeur du perso : 90
        // Hauteur du perso : 90
        
        if(xLink < -90){//Collision gauche
            xLink = 1800;
        }
        
        if(xLink > 1800){//Collision droite
            xLink = -90;
        }
        
        if(yLink < -90){//Collision haute
            yLink = 1038;
        }
        
        if(yLink > 1038){//Collision basse
            yLink = -90;
        } 
    }
    
