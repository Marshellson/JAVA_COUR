    private void jPanel1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPanel1KeyPressed

        if (evt.getKeyChar() == 'z') {//Vers le haut
            indiceLink = 10;
            compteurLink += 1;
            yLink -= 10;
        }
        if (evt.getKeyChar() == 'q') {//Vers la gauche
            indiceLink = 0;
            compteurLink += 1;
            xLink -= 10;
        }
        if (evt.getKeyChar() == 's') {//Vers le bas
            indiceLink = 15;
            compteurLink += 1;
            yLink += 10;
        }
        if (evt.getKeyChar() == 'd') {//Vers la droite
            indiceLink = 5;
            compteurLink += 1;
            xLink += 10;
        }
        
        if(compteurLink == 5){
            compteurLink = 0;
        }
        
        traitementBornes();
        traitementPotion();
        jPanel1.repaint();
    }//GEN-LAST:event_jPanel1KeyPressed

